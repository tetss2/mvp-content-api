function sentenceForIntent(intent) {
  const map = {
    educational_post: "Разберем тему спокойно: что здесь важно понимать, где часто появляется путаница и какой первый бережный шаг можно сделать.",
    storytelling: "Иногда человек приходит не с готовым вопросом, а с ощущением: со мной что-то не так, хотя на самом деле внутри давно живет напряжение.",
    faq_answer: "Короткий ответ: универсальной нормы здесь нет, но есть ориентиры, по которым можно понять, что именно происходит.",
    sales_post: "Если вы узнаете в этом себя, консультация может стать местом, где получится разобрать ситуацию без стыда и давления.",
    short_hook: "Женская сексуальность не исчезает без причины. Чаще она замолкает там, где стало небезопасно хотеть.",
    therapeutic_case: "В условном примере женщина говорит: я понимаю головой, что хочу близости, но тело как будто закрывается.",
  };
  return map[intent] || map.educational_post;
}

function structureForIntent(intent) {
  if (intent === "short_hook") {
    return [
      "1. Сексуальность не обязана быть громкой, чтобы быть живой.",
      "2. Иногда вопрос не в желании, а в безопасности рядом с ним.",
      "3. Тело не спорит с вами. Оно показывает, где ему страшно.",
      "4. Если близость стала обязанностью, желание часто уходит первым.",
      "5. Нежность к себе тоже часть сексуальности.",
    ].join("\n");
  }

  if (intent === "faq_answer") {
    return [
      "Короткий ответ: это не всегда проблема и не всегда патология.",
      "",
      "Важно смотреть на контекст: изменилось ли желание резко, есть ли боль, напряжение, стыд, усталость, конфликт в отношениях или страх близости. В сексологии мы не оцениваем человека по одной реакции. Мы смотрим на систему факторов.",
      "",
      "Практический шаг: попробуйте честно отделить желание от обязанности. Что я хочу сама? Что я делаю из страха обидеть, потерять, не соответствовать?",
      "",
      "К специалисту стоит обратиться, если тема вызывает сильную тревогу, боль, избегание близости или повторяющийся конфликт в паре.",
    ].join("\n");
  }

  return [
    sentenceForIntent(intent),
    "",
    "У женской сексуальности много слоев: телесный, эмоциональный, relational, культурный. Поэтому с ней редко помогает грубая инструкция вроде \"просто расслабься\". Намного точнее спрашивать: что сейчас происходит с безопасностью, доверием, стыдом, усталостью и правом хотеть по-своему?",
    "",
    "Например, снижение желания может быть не отказом от близости, а способом психики защититься от давления. И тогда путь начинается не с требования к себе, а с внимательного исследования: где я теряю контакт с собой?",
    "",
    "Если откликается, сохраните этот текст и вернитесь к нему спокойно. А если тема давно болит, ее можно бережно разобрать в консультации.",
  ].join("\n");
}

async function generateWithMockAdapter({ plan }) {
  const intent = plan?.generation_intent || "educational_post";
  return {
    provider: "mock",
    model: "local-deterministic-mock",
    output: structureForIntent(intent),
    usage: null,
    warnings: ["mock_adapter_used"],
  };
}

