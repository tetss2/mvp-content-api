export const SEXOLOGIST_STYLE_ALIASES = {
  simple: "friendly",
  friend: "friendly",
  girlfriends_talk: "girlfriends",
  girlfriend: "girlfriends",
};

export function normalizeSexologistStyleKey(styleKey = "auto") {
  const rawKey = String(styleKey || "auto").trim();
  const mappedKey = SEXOLOGIST_STYLE_ALIASES[rawKey] || rawKey;
  return SEXOLOGIST_STYLE_META[mappedKey] ? mappedKey : "auto";
}

